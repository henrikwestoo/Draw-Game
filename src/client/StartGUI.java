    private void playBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playBtnActionPerformed

        //default
        String ip = "127.0.0.1";
        int port = 2000;

        if (!ipTxt.getText().isEmpty()) {

            ip = ipTxt.getText();
        }

        if (!portTxt.getText().isEmpty()) {

            try {
                port = Integer.valueOf(portTxt.getText());
            } catch (NumberFormatException e) {

                errorLbl.setText("Invalid port input");

            }
        }
        
        //skapa socket

        try {
            Socket socket = new Socket(ip, port);

            //skapa klienttr√•d
            ClientThread clientThread = new ClientThread(socket);

            System.out.println(ip + " : "+ port);
            
            //skapa gui
            Paper paper = new Paper(clientThread);
            clientThread.paper = paper;
            ClientGUI gui = new ClientGUI(paper, clientThread);
            clientThread.gui = gui;

            clientThread.start();
            this.dispose();

        } catch (IOException ex) {
            
            errorLbl.setText("A connection could not be made");
        }

    }//GEN-LAST:event_playBtnActionPerformed


